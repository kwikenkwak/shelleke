//! Hand-authored 1-bit pixel-art sprites.
//!
//! Each sprite is written as ASCII rows where `#` = a black pixel and any other
//! char (`.` by convention) = transparent. They are blitted 1:1 onto the
//! canvas, so what you draw here is exactly what ends up on screen — crisp,
//! deliberate pixel art, no rasteriser anti-aliasing involved.
//!
//! Several distinct variants are provided per element; the renderer picks one
//! per feature deterministically by hash, and may also mirror it horizontally,
//! so the world looks varied while staying perfectly stable as it scrolls.

use bevy::prelude::Resource;

/// A small 1-bit bitmap.
pub struct Sprite {
    pub w: usize,
    pub h: usize,
    bits: Vec<bool>,
}

impl Sprite {
    /// Build from ASCII rows. Rows may differ in length; the width is the
    /// longest row and short rows are padded with transparency.
    pub fn from_rows(rows: &[&str]) -> Sprite {
        let h = rows.len();
        let w = rows.iter().map(|r| r.chars().count()).max().unwrap_or(0);
        let mut bits = vec![false; w * h];
        for (y, row) in rows.iter().enumerate() {
            for (x, c) in row.chars().enumerate() {
                if c == '#' {
                    bits[y * w + x] = true;
                }
            }
        }
        Sprite { w, h, bits }
    }
    #[inline]
    pub fn get(&self, x: usize, y: usize) -> bool {
        x < self.w && y < self.h && self.bits[y * self.w + x]
    }
}

/// All sprite variants, built once at startup.
#[derive(Resource)]
pub struct Library {
    pub mountain: Vec<Sprite>,
    pub forest: Vec<Sprite>,
    pub tree: Vec<Sprite>,
    pub village: Vec<Sprite>,
    pub city: Vec<Sprite>,
    pub castle: Vec<Sprite>,
    pub monastery: Vec<Sprite>,
    pub sheep: Sprite,
    pub bird: Vec<Sprite>,
}

impl Library {
    pub fn build() -> Library {
        Library {
            mountain: mountains(),
            forest: forests(),
            tree: trees(),
            village: villages(),
            city: cities(),
            castle: castles(),
            monastery: monasteries(),
            sheep: sheep(),
            bird: birds(),
        }
    }
}

